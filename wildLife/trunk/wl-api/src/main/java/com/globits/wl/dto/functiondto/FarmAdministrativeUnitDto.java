package com.globits.wl.dto.functiondto;



public class FarmAdministrativeUnitDto {
	private String nameAu;
	private String codeAu;
	private String mapCodeAu;
	private Long idAu;
	private double quantity;// số lượng
	private double totalAcreage;//diện tích tỉnh
	private double count;//số con/km2 
	private Long regionId;//id vùng
	private String regionName; //tên vùng
	
	public String getNameAu() {
		return nameAu;
	}
	public void setNameAu(String nameAu) {
		this.nameAu = nameAu;
	}
	public String getCodeAu() {
		return codeAu;
	}
	public void setCodeAu(String codeAu) {
		this.codeAu = codeAu;
	}
	public String getMapCodeAu() {
		return mapCodeAu;
	}
	public void setMapCodeAu(String mapCodeAu) {
		this.mapCodeAu = mapCodeAu;
	}
	public Long getIdAu() {
		return idAu;
	}
	public void setIdAu(Long idAu) {
		this.idAu = idAu;
	}
	
	public double getTotalAcreage() {
		return totalAcreage;
	}
	public void setTotalAcreage(double totalAcreage) {
		this.totalAcreage = totalAcreage;
	}
	public double getQuantity() {
		return quantity;
	}
	public void setQuantity(double quantity) {
		this.quantity = quantity;
	}
	public double getCount() {
		return count;
	}
	public void setCount(double count) {
		this.count = count;
	}
	public Long getRegionId() {
		return regionId;
	}
	public void setRegionId(Long regionId) {
		this.regionId = regionId;
	}
	public String getRegionName() {
		return regionName;
	}
	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}			
}
