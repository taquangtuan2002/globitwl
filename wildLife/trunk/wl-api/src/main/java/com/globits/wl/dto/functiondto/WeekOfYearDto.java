package com.globits.wl.dto.functiondto;

import java.util.Date;

public class WeekOfYearDto {
	public int orderNumber;	//thứ tự tuần của năm
	public Date startDate;	//ngày bắt đầu
	public Date endDate;	//ngày kết thúc
	
	public String name;	//hiển thị
	
	public int getOrderNumber() {
		return orderNumber;
	}
	public void setOrderNumber(int orderNumber) {
		this.orderNumber = orderNumber;
	}
	public Date getStartDate() {
		return startDate;
	}
	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}
	public Date getEndDate() {
		return endDate;
	}
	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

}
